let isEligible = false;
let timer = null;
const requiredTime = 10; // 必要な滞在時間（秒）
let elapsedTime = 0;
let hankei = 100;
let ido = 35.64498042238834;
let keido = 139.4083764643375;

// 地図の初期化
let map;
let userMarker;
let circle;
function initializeMap() {
    const targetLat = ido; // 指定された緯度
    const targetLon = keido; // 指定された経度
    const radius = hankei; // メートル

    // 地図を表示
    map = L.map('map').setView([targetLat, targetLon], 15);

    // 地図タイルを追加
    L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
        attribution: '&copy; OpenStreetMap contributors'
    }).addTo(map);

    // ロケーション範囲を円で表示
    circle = L.circle([targetLat, targetLon], {
        color: 'blue',
        fillColor: '#aaf',
        fillOpacity: 0.2,
        radius: hankei
    }).addTo(map);
}

function startLocationTracking() {
    if (navigator.geolocation) {
        navigator.geolocation.watchPosition(success, error, {
            enableHighAccuracy: true,
            maximumAge: 0,
            timeout: 5000
        });
    } else {
        statusDiv.innerText = "このブラウザは位置情報をサポートしていません。";
    }
}

function success(position) {
    const { latitude, longitude } = position.coords;
    const targetLat = ido; // 指定された緯度
    const targetLon = keido; // 指定された経度
    const radius = hankei; // メートル
    // 地図上にユーザーの位置を表示
    if (!userMarker) {
        userMarker = L.marker([latitude, longitude]).addTo(map);
    } else {
        userMarker.setLatLng([latitude, longitude]);
    }

    // ユーザーの位置に応じて地図のビューを更新
    map.setView([latitude, longitude]);

    const distance = getDistanceFromLatLonInMeters(latitude, longitude, targetLat, targetLon);
    if (distance <= radius) {
        if (!timer) {
            startTimer(); // エリア内ならタイマーを開始
        }
    } else {
        if (timer) {
            clearInterval(timer); // タイマーを停止
            timer = null;
            elapsedTime = 0; // 経過時間をリセット
            isEligible = false; // 報酬受取状態をリセット
            claimRewardButton.style.display = 'none'; // ボタンを非表示
        }
        statusDiv.innerText = "指定された場所にいません。";
    }
}


function error(err) {
    console.warn(`ERROR(${err.code}): ${err.message}`);
    statusDiv.innerText = "位置情報の取得に失敗しました。";
}

function getDistanceFromLatLonInMeters(lat1, lon1, lat2, lon2) {
    function deg2rad(deg) {
        return deg * (Math.PI / 180);
    }
    const R = 6371e3; // 地球の半径（メートル）
    const dLat = deg2rad(lat2 - lat1);
    const dLon = deg2rad(lon2 - lon1);
    const a =
        Math.sin(dLat / 2) * Math.sin(dLat / 2) +
        Math.cos(deg2rad(lat1)) *
        Math.cos(deg2rad(lat2)) *
        Math.sin(dLon / 2) *
        Math.sin(dLon / 2);
    const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
    const distance = R * c;
    return distance;
}

function startTimer() {
    // 定期的に滞在時間をチェック
    timer = setInterval(() => {
        elapsedTime += 1;
        const remainingTime = requiredTime - elapsedTime;
        if (remainingTime > 0) {
            statusDiv.innerText = `指定された場所にいます。報酬を受け取るにはあと ${remainingTime} 秒滞在してください。`;
        } else {
            statusDiv.innerText = "報酬を受け取る準備ができました。ボタンをクリックしてください。";
            isEligible = true; // 必要な滞在時間を満了したのでフラグを更新
            clearInterval(timer); // タイマーをクリア
            timer = null;
            claimRewardButton.style.display = 'block'; // 報酬ボタンを再表示
        }
    }, 1000); // 1秒ごとにチェック
}


